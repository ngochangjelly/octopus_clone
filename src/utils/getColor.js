export const getColor = key => {
  switch (key) {
    case 0:
      return 'rgb(234, 94, 94)';
    case 1:
      return 'rgb(130, 197, 72)';
    case 2:
      return 'rgb(234, 196, 94)';
    case 3:
      return 'rgb(94, 210, 234)';
    case 4:
      return 'rgb(230, 94, 234)';
    case 5:
      return 'rgb(94, 234, 200)';
    case 6:
      return 'rgb(94, 129, 234)';
    case 7:
      return 'rgb(244, 160, 7)';
    default:
      return 'rgb(234, 94, 94)';
  }
};
